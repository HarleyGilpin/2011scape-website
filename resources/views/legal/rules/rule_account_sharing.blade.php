                  <p>We have this rule for two reasons:</p> 
                  <ol> 
                   <li>Firstly, many of the accounts people try to sell are actually accounts they have stolen, so, by buying the account you are helping them profit from their crime, and encouraging them to do it more!</li> 
                   <li>Secondly, this rule is for your protection, as people who share accounts run the risk that the person using the account won't give it back, or that the person will break a game rule causing your account to be banned or muted.</li> 
